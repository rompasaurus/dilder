def draw_body(rle):
    """Render an RLE body shape into the frame buffer with transforms."""
    for y, runs in rle:
        for x0, x1 in runs:
            ax0 = x0 - canvas.body_x_expand
            ax1 = x1 + canvas.body_x_expand
            if ax0 < 0:
                ax0 = 0
            if ax1 >= canvas.IMG_W:
                ax1 = canvas.IMG_W - 1
            for x in range(ax0, ax1 + 1):
                canvas.px_set_off(x, y)
